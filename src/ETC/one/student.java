package ETC.one;

public class student {

    private String name;

    private String no;

    public student(String name, String no) {
        this.name = name;
        this.no = no;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }


}